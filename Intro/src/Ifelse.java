public class Ifelse {
    public static void main(String[] args) {
        boolean hungry = false;

        if (hungry) {
            System.out.println("Hungry");
        }
        else
        {
            System.out.println("Not hungry");
        }

        int hunting = 5;
        if (hunting < 6)
        {
            System.out.println("Hungry");
        }
        else
        {
            System.out.println("Not hungry");
        }

    }
}
