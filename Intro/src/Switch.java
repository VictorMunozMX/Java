public class Switch {
    public static void main(String[] args) {

        int hunting = 5;
        switch (hunting) {
            case 1:
                System.out.println("I'm 1!");
                break;
            case 2:
                System.out.println("I'm 2!");
                break;
            case 3:
                System.out.println("I'm 3!");
                break;
            default:
                System.out.println("I'm Out!");
        }
    }
}
